package com.globalpaysolutions.yocomprorecarga.models;


public class CountryOperator
{

    private Integer operatorID;
    private String name;
    private String brand;
    private String mnc;
    private String operatorLogo;
    private String logoUrl;
    private Integer logoVersion;
    private String hexColor;
    private String rGBColor;
    private Integer relevance;

    public Integer getOperatorID()
    {
        return operatorID;
    }

    public void setOperatorID(Integer operatorID)
    {
        this.operatorID = operatorID;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getBrand()
    {
        return brand;
    }

    public void setBrand(String brand)
    {
        this.brand = brand;
    }

    public String getMnc()
    {
        return mnc;
    }

    public void setMnc(String mnc)
    {
        this.mnc = mnc;
    }

    public String getOperatorLogo()
    {
        return operatorLogo;
    }

    public void setOperatorLogo(String operatorLogo)
    {
        this.operatorLogo = operatorLogo;
    }

    public String getLogoUrl()
    {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl)
    {
        this.logoUrl = logoUrl;
    }

    public Integer getLogoVersion()
    {
        return logoVersion;
    }

    public void setLogoVersion(Integer logoVersion)
    {
        this.logoVersion = logoVersion;
    }

    public String getHexColor()
    {
        return hexColor;
    }

    public void setHexColor(String hexColor)
    {
        this.hexColor = hexColor;
    }

    public String getRGBColor()
    {
        return rGBColor;
    }

    public void setRGBColor(String rGBColor)
    {
        this.rGBColor = rGBColor;
    }

    public Integer getRelevance()
    {
        return relevance;
    }

    public void setRelevance(Integer relevance)
    {
        this.relevance = relevance;
    }

}
